Import-Module -Name 'Semperis-Lib'

$outputObjects = [System.Collections.ArrayList]@()
$unavailableDomains = [System.Collections.ArrayList]@()

try {
    if ($PSBoundParameters['ForestName'] -and $PSBoundParameters['DomainNames']) {
        $ForestName = $ForestName.ToLower()
        $DomainNames = ConvertTo-Lowercase -DomainNames $DomainNames
    }
    $res = New-Result

    $sIDHistoryUsers = @()

    foreach ($domain in $DomainNames) {
        if ($unavailableDomains.Contains($domain)) {
            continue
        }
        elseif (-not (Confirm-DomainAvailability $domain)) {
            [void]$unavailableDomains.Add($domain)
            continue
        }
        $results = [System.Collections.ArrayList]@()
        $DN = Get-DN $domain

        # Get a list of all users with SIDHistory
        $searchParams = @{
            dnsDomain = $domain
            attributes = "sIDHistory"
            baseDN = $DN
            scope = "Subtree"
            filter = "(sIDHistory=*)"
            PropertyFilter = "sIDHistory"
            StartAttackWindow = $StartAttackWindow
            EndAttackWindow = $EndAttackWindow
        }

        $sIDHistoryUsers += Search-ADHelper @searchParams
    }

    if ($sIDHistoryUsers) {
        $forestPrivilegedGroups = @("519","527","518")
        $forestDN = Get-DN $ForestName
        $forestPrivilegedGroupsDN = @()

        # Init a list of forest privileged users' SID
        $adminsSID = [System.Collections.ArrayList]@()

        if (Confirm-DomainAvailability $ForestName) {
            $forestSID = Get-DomainSID $ForestName
            foreach ($groupRID in $forestPrivilegedGroups) {
                $groupPair = "$forestSID-$groupRID"
                [void]$adminsSID.Add((New-Object System.Security.Principal.SecurityIdentifier $groupPair))
                $forestGroupObject = Search-AD -dnsDomain $ForestName -attributes "" -baseDN $forestDN -scope "Subtree"`
                    -filter "(&(objectSid=$forestSID-$groupRID)(objectCategory=group))"
                if ($forestGroupObject) {
                    $forestPrivilegedGroupsDN += $forestGroupObject.DistinguishedName
                }
            }
        }
        else {
            $DomainNames = $DomainNames | Where-Object {$ForestName -notcontains $_}
            [void]$unavailableDomains.Add($ForestName)
        }


        foreach ($domain in $DomainNames) {
            if ($unavailableDomains.Contains($domain)) {
                continue
            }
            elseif (-not (Confirm-DomainAvailability $domain)) {
                [void]$unavailableDomains.Add($domain)
                continue
            }
            $DN = Get-DN $domain
            $domainSID = Get-DomainSID $domain
            $privilegedGroupsDN = $forestPrivilegedGroupsDN

            # Get a list of privileged groups DN
            $privilegedGroups = @("512","516","526","521")
            foreach ($groupRID in $privilegedGroups) {
                $groupPair = "$domainSID-$groupRID"
                [void]$adminsSID.Add((New-Object System.Security.Principal.SecurityIdentifier $groupPair))
                $groupObject = Search-AD -dnsDomain $domain -attributes "" -baseDN $DN -scope "subtree" `
                    -filter "(&(objectSid=$domainSID-$groupRID)(objectCategory=group))"
                if ($groupObject) {
                    $privilegedGroupsDN += $groupObject.DistinguishedName
                }
            }

            # Get a list of builtin privileged groups dn
            $builtinPrivilegedGroups = @("S-1-5-32-551","S-1-5-32-552","S-1-5-32-548","S-1-5-32-549","S-1-5-32-550","S-1-5-32-544")
            foreach ($sid in $builtinPrivilegedGroups) {
                [void]$adminsSID.Add((New-Object System.Security.Principal.SecurityIdentifier $sid))
                $groupObject = Search-AD -dnsDomain $domain -attributes "" -baseDN $DN -scope "subtree" `
                    -filter "(&(objectSid=$sid)(objectCategory=group))"
                if ($groupObject) {
                    $privilegedGroupsDN += $groupObject.DistinguishedName
                }
            }

            $filter = "(&(objectCategory=person)(|"
            foreach ($groupDN in $privilegedGroupsDN) {
                $filter += "(memberOf:1.2.840.113556.1.4.1941:=$groupDN)"
            }
            $filter += "))"

            $membersOfPrivilegedGroups = Search-AD -dnsDomain $domain -attributes "objectSid" -baseDN $DN -scope "Subtree" -filter $filter
            foreach ($privilegedUser in $membersOfPrivilegedGroups) {
                [void]$adminsSID.Add((New-Object System.Security.Principal.SecurityIdentifier @($privilegedUser.Attributes."objectsid".GetValues("byte[]")[0],0)))
            }
        }
    }

    # Check each SID in each user's SIDHistory to see if it matches an admin's SID
    foreach ($sIDHistoryUser in $sIDHistoryUsers) {
        $addUser = $false
        foreach ($SID in $sIDHistoryUser.Attributes."sidhistory") {
            $SIDobject = New-Object System.Security.Principal.SecurityIdentifier @($SID,0)
            if ($SIDobject -in $adminsSID) {
                $addUser = $true
            }
        }
        if ($addUser -eq $true) {
            [void]$results.Add($sIDHistoryUser)
            continue
        }
    }

    # Translate the SID from byte array to string
    if ($results.Count) {
        foreach ($result in $results) {
            $sIDHistory = @()
            foreach ($sid in $result.Attributes."sidhistory") {
                $SIDobject = New-Object System.Security.Principal.SecurityIdentifier @($sid,0)
                if ($SIDobject -in $adminsSID) {
                    $SIDHistory += $SIDobject.Value
                }
            }
            $thisOutput = new-object psobject -Property @{
                DistinguishedName = $result.DistinguishedName
                SIDHistory = $sIDHistory -join ";"
            }
            $attrMetaData = $result.Attributes.'attributemetadata'
            Update-IndicatorOutputAttackWindow -ThisOutput $thisOutput -Metadata $attrMetaData `
                -StartAttackWindow $StartAttackWindow -EndAttackWindow $EndAttackWindow -excludeTimestamp
            [void]$outputObjects.Add($thisOutput)
        }
    }


    # Count is the number of domains that had a user with privileged SID in his SIDHistory
    if ($outputObjects) {
        $configArgs = @{
            ScriptName = $self.ScriptName
            Path = $MyInvocation.MyCommand.ScriptBlock.File
            Fields = $outputObjects[0]
        }
        $config = Resolve-Configuration @configArgs
        $outputObjects | Set-IgnoredFlag -Configuration $config
        $scoreOutput = $outputObjects | Get-Score -Impact $self.Impact
        if ($scoreOutput.Score -lt 100) {
            $res.ResultObjects = $outputObjects
            $res.ResultMessage = "Found $($outputObjects.Count) objects with privileged SIDs inside their SIDHistory."
            $res.Remediation = "Review the list and remove SIDs from the SIDHistory attribute if not explicitly required for migration. Unidentified users with privileged SIDs in SIDHistory should be investigated."
            $res.Status = 'Failed'
            $res.Score = $scoreOutput.Score
        }
        if ($scoreOutput.Ignoredcount -gt 0) {
            $res.ResultMessage += " ($($scoreOutput.Ignoredcount) Objects ignored)."
            $res.ResultObjects = $outputObjects
        }
    }

    # deal with unavailable domains
    if ($unavailableDomains.Count -gt 0) {
        if ($unavailableDomains.Count -eq $DomainNames.Count) {
            $res.Score = 0
            $res.Status = 'Error'
            $res.ResultMessage = "The following domains were unavailable: $($unavailableDomains -join ', ')."
            $res.Remediation = "None"
        }
        else {
            $res.ResultMessage += " The following domains were unavailable: $($unavailableDomains -join ', ')."
        }
    }
}

catch {
    return ConvertTo-ErrorResult $_
}
return $res

